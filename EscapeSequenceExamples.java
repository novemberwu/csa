public class EscapeSequenceExamples {
        public static void main(String[] args) {
            System.out.println("Hello,\nWorld!"); // Newline
            System.out.println("Name:\tJohn Doe"); // Tab
            System.out.println("This is a backslash: \\"); // Literal backslash
            System.out.println("He said, \"Hello!\""); // Double quote within string
            System.out.println('\''); // Single quote in a char literal
        }

}
